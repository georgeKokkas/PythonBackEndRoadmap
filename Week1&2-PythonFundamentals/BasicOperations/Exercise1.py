# Write a program to perform basic arithmetic and string operations.

# Arithmetic operations
sum = 10 + 5
difference = 10 - 5
product = 10 * 5
quotient = 10 / 2
exponent = 2 ** 3
modulo = 10 % 3

# String operations
greeting = "Hello, " + "World!"
repeated_greeting = "Hello" * 3
substring = greeting[0:5]

# Print out everything in the console
print(sum) # Output: 15
print(difference) # Output : 5
print(product) # Output : 50
print(quotient) # Output : 5.0
print(exponent) # Output : 8
print(modulo) # Output : 1

print(greeting) # Output : Hello, World!
print(repeated_greeting) # Output : HelloHelloHello
print(substring) # Output : Hello