# Create a list of your favorite books and perform basic list operations.

# Create a list of favorite books
favorite_books = ["1984", "To Kill a Mockingbird", "The Great Gatsby", "Moby Dick"]

# Add a book to the list
favorite_books.append("Pride and Prejudice")

# Remove a book from the list
favorite_books.remove("Moby Dick")

# Access the first book in the list
first_book = favorite_books[0]

# Print the updated list and the first book
print(favorite_books)  # Output: ['1984', 'To Kill a Mockingbird', 'The Great Gatsby', 'Pride and Prejudice']
print(first_book)  # Output: 1984
