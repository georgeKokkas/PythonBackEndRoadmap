# Create a set of unique colors and add/remove colors.

# Create a set of unique colors
unique_colors = {"red", "green", "blue"}

# Add a new color to the set
unique_colors.add("yellow")

# Remove a color from the set
unique_colors.remove("green")

# Print the updated set
print(unique_colors)  # Output: {'red', 'blue', 'yellow'}
